    KhuyenMai checkFormVC() {
        KhuyenMai km = new KhuyenMai();

        String tenVC = txtTenVC.getText();
        String maVC = txtMaVC.getText();
        Date ngayBD = DateNgayBD.getDate();
        Date ngayKT = DateNgayKT.getDate();
        String loaiVC = cboLoaiVoucher.getSelectedItem().toString();
        String giaTriGiam = txtGiaTriGiam.getText();
        String giaTriDonhangToiThieu = GiaTriToiThieu.getText();
        String sl = txtSoLuong.getText();
        if (tenVC.isEmpty() || maVC.isEmpty() || ngayBD.equals("") || ngayKT.equals("") || loaiVC.isEmpty() || giaTriGiam.isEmpty() || giaTriDonhangToiThieu.isEmpty() || sl.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Vui lòng điền đầy đủ thông tin!");
            return null;
        }
//        if (ngayBD.before(today) && ngayKT.before(today) || ngayBD.after(ngayKT) || ngayBD.before(today) || ngayKT.before(today)) {
//            JOptionPane.showMessageDialog(null, "Nhập sai ngày bắt đầu và ngày kết thúc!");
//            return null;
//        }

        try {
            int giatriGiamInt = Integer.parseInt(giaTriGiam);
            int giatriDHInt = Integer.parseInt(giaTriDonhangToiThieu);
            int slInt = Integer.parseInt(sl);
            if (giatriGiamInt <= 0 || giatriDHInt <= 0 || slInt <= 0) {
                JOptionPane.showMessageDialog(null, "Vui lòng nhập số nguyên dương: giá trị giảm, Giá trị đơn hàng, số lượng");
            } else {
                km.setTenVC(tenVC);
                km.setMaKM(maVC);
                km.setNgayBatDau(ngayBD);
                km.setNgayKetThuc(ngayKT);
                km.setHinhThucGiamGia(loaiVC);
                km.setMucGiamGia(giaTriGiam);
                km.setGiaTriDHToiThieu(giatriDHInt);
                km.setSoLuong(slInt);
                km.setNguoiSua("NV001");
                km.setNguoiTao("NV001");

//                if ( ngayBD.equals(today)) {
//                    km.setTrangThai("Đang diễn ra");
//                }
                
                if (ngayBD.before(today) && ngayKT.after(today) || ngayBD.equals(today) && ngayKT.equals(today) || ngayBD.equals(today) && ngayKT.after(today)) {
                    km.setTrangThai("Đang diễn ra");
                } else if (ngayBD.after(today) && ngayKT.after(today)) {
                    km.setTrangThai("Sắp diễn ra");
                } else {
                    km.setTrangThai("Đã hết hạn");
                }
                return km;
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Vui lòng nhập số nguyên dương: giá trị giảm, Giá trị đơn hàng, số lượng");
        }
        return km;
    }

