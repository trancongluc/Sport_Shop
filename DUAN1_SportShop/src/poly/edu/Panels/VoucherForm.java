    void insertKM() {
        KhuyenMai km = checkformKhuyenMai();
        String mucGiam = txtMucKM.getText();
        if (km == null) {
            return;
        }

        try {
            ArrayList<SanPhamChiTiet> selectedKM = new ArrayList<>();
            getIDSPCTTich(selectedKM);

            System.out.println(selectedKM);
            for (SanPhamChiTiet spct : selectedKM) {
                km.setId_SPCT(spct.getId());
                if (cboHinhthucKM.getSelectedItem().equals("Giảm theo %")) {
                    km.setSoTienGiam(spct.getGia() * Integer.parseInt(mucGiam) / 100);
                } else {
                    km.setSoTienGiam(Integer.parseInt(txtMucKM.getText()));
                }
            }
            km.setTrangThai("Thành công");
            km.setNguoiSua("NV001");
            km.setNguoiTao("NV001");
            km.setDeleted(true);
            vs.insertKM(km);
            JOptionPane.showMessageDialog(null, "Thành công!");
            LoadDataToTable();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Thất bại");
        }
    }

